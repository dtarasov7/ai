# HPBN RU - Современное дополнение после 2016 года

- High Performance Browser Networking: современное дополнение после 2016 года
  - Центральная идея дополнения
    - Главный тезис книги не изменился
      - производительность веба определяется не только bandwidth
      - решающее значение имеют стоимость установления соединения
      - влияют потери, очереди и политика приоритизации
      - важна скорость достижения первых полезных байтов
    - После 2016 года изменился transport-слой
      - появились HTTP/3 и QUIC как новая транспортная основа браузерного веба
      - TLS 1.3 сократил цену безопасного старта
      - 0-RTT добавил ускорение для повторных безопасных запросов
      - BBR изменил взгляд на congestion control
      - ECH изменил видимость ранней фазы TLS
      - диагностика QUIC сместилась от wire capture к endpoint logs и qlog
      - fingerprinting стал частью реального delivery path
    - Общая формула HPBN сохраняется
      - выигрываем не за счёт "магии нового протокола"
      - выигрываем за счёт сокращения лишних RTT
      - выигрываем за счёт уменьшения блокировок
      - выигрываем за счёт более предсказуемого поведения под потерями
  - Опорная рамка чтения
    - Базовые термины
      - RTT = полный сетевой обход туда и обратно
      - critical path = минимальная цепочка шагов до следующего полезного действия браузера
      - origin = сайт или backend, к которому идёт запрос
      - stream = отдельный логический поток данных внутри соединения
    - Практический вопрос для любой технологии
      - ускоряет ли она путь до первых полезных байтов
      - делает ли она путь надёжнее под потерями
  - Почему дополнение понадобилось
    - В 2016 году фокус был на HTTP/2, TLS 1.2, TCP Fast Open, packet loss и очередях
    - Сегодня базовая цель та же
      - браузер должен быстро дойти до first meaningful paint
      - после старта страница не должна терять темп при доставке зависимостей
    - Но критический путь стал другим
      - HTTP/3 работает поверх QUIC
      - QUIC использует UDP и шифруется через TLS 1.3
      - в critical path вошли discovery альтернативного транспорта и новая приоритизация
    - Правильный вопрос современного HPBN
      - не "какой протокол новее"
      - а "какие задержки и блокировки этот протокол убирает"
      - и "какие ограничения он оставляет"
    - Ограничения сети никуда не исчезли
      - QUIC не уменьшает физическое расстояние
      - QUIC не убирает перегрузку
      - QUIC не превращает плохой radio path в хороший
      - QUIC меняет форму потерь, стоимость повтора, flow control и resume-поведение
    - Практический эффект для браузера
      - деградация становится более плавной
      - становится меньше ситуаций, когда одна потеря тормозит всё сразу
  - Часть I: новый transport для веба
    - QUIC как новый транспорт для браузерного веба
      - Главная идея QUIC
        - это не просто "HTTP поверх UDP"
        - transport, безопасность и управление потоками спроектированы совместно
      - Сравнение со старым стеком
        - в TCP + TLS + HTTP/2 транспорт знает только о байтах
        - TLS знает о записях
        - HTTP знает о потоках
      - Что QUIC даёт сразу на transport-уровне
        - мультиплексирование потоков
        - flow control
        - loss recovery
        - migration при смене сети
        - встроенный криптографический контекст TLS 1.3
      - Главное следствие для производительности
        - потеря пакета больше не блокирует все HTTP-потоки целиком на transport-уровне
        - потеря всё ещё уменьшает доступную полосу
        - доставка конкретных данных может сдвигаться во времени
        - но исчезает классический connection-wide head-of-line blocking TCP
      - Migration как практический выигрыш
        - соединение привязано к connection ID, а не только к паре IP:port
        - при переходе с Wi-Fi на LTE есть шанс продолжить текущее соединение
        - это особенно важно для мобильных клиентов
      - Производственный смысл QUIC
        - особенно полезен на шумных путях
        - помогает при высоком jitter
        - помогает на мобильных устройствах
        - особенно ценен для страниц с большим числом зависимостей разной критичности
    - HTTP/3 как переразметка HTTP поверх QUIC
      - Семантика HTTP не меняется
        - браузер всё так же отправляет запросы и получает ответы
        - меняется способ укладки сообщений в transport
      - Архитектурное отличие
        - HTTP/2 использовал бинарный фрейминг поверх TCP
        - HTTP/3 использует бинарный фрейминг поверх QUIC streams
      - Практический смысл для HPBN
        - несколько ответов могут передаваться без зависимости от глобального порядка байтов единого TCP-потока
        - неблагоприятная сеть меньше разрушает поведение всей страницы
      - Что не исчезает полностью
        - application-level зависимости всё ещё остаются
        - служебная информация и данные всё ещё могут блокировать друг друга
        - QPACK уменьшает риск блокировок заголовков
        - но требует осторожного управления зависимостями между header state и body delivery
      - Реальный эффект HTTP/3
        - он не исправляет плохой critical path страницы
        - не заменяет грамотную иерархию ресурсов
        - не отменяет важность приоритизации
        - зато делает систему устойчивее к loss, reorder и смене пути
      - Простое резюме
        - HTTP/3 не "ускоряет всё"
        - он уменьшает число случаев, когда одна проблема сети стопорит все ответы сразу
    - Новая цена RTT: 1-RTT, resumption и 0-RTT
      - Самый ценный вклад QUIC и TLS 1.3
        - не только мультиплексирование
        - но и сокращение цены безопасного установления соединения
      - Поведение нового соединения
        - QUIC использует TLS 1.3 для key exchange
        - новое соединение без потерь обычно укладывается в 1 RTT до защищённого канала
      - Поведение повторного соединения
        - возможен resumption
        - иногда возможна передача application data в 0-RTT
      - Ограничения 0-RTT
        - это не бесплатное ускорение для любого сценария
        - ранние данные уязвимы к replay
        - подходят не для любого запроса и не для любой бизнес-операции
      - Полезная модель мышления
        - 0-RTT надо рассматривать как ускоритель безопасно повторяемых операций
        - QUIC + TLS 1.3 уже сами по себе снижают стартовую цену по сравнению с TCP + TLS 1.2
      - Связь с философией HPBN
        - раньше считали RTT между SYN, SYN-ACK, ClientHello и первым байтом ответа
        - теперь считаем RTT между Initial, Handshake и первым полезным stream
        - вопрос не изменился: сколько полных сетевых обходов нужно до полезного действия браузера
    - mTLS: где он помогает, а где только удорожает путь
      - Базовая идея
        - обычный TLS аутентифицирует сервер
        - mTLS добавляет аутентификацию клиента
        - сервер просит клиентский сертификат
        - клиент подтверждает владение приватным ключом
      - Что это значит для HPBN
        - это сильный security-механизм
        - но это ещё один слой требований к critical path соединения
      - Где mTLS полезен в браузерных приложениях
        - корпоративные порталы
        - B2B-панели
        - админские интерфейсы
        - zero-trust сегменты
        - внутренние API за браузерным фронтом
      - Почему в этих сценариях mTLS может окупаться
        - важнее сильная идентификация устройства или пользователя
        - контролируемая выдача сертификатов возможна
        - цена ошибки доступа высока
      - Что именно mTLS гарантирует
        - сертификат клиента из доверенной цепочки
        - сертификат действителен и пригоден для client authentication
        - клиент владеет соответствующим приватным ключом
        - transport получает доверенную cryptographic identity
      - Чего mTLS сам по себе не гарантирует
        - не доказывает "реальную личность" без хорошей PKI
        - не заменяет нормальную авторизацию
        - не решает проблему украденного приватного ключа
        - не определяет tenant, роль или business-domain автоматически
      - Почему этого недостаточно без policy-слоя
        - привязка identity к пользователю, устройству, сервису или tenant живёт в локальной логике
        - сервер или прокси может смотреть SAN, subject, issuer, policy OID, fingerprint
        - но может и не делать строгую привязку
      - Практический вывод
        - mTLS даёт transport-фундамент безопасности
        - авторизацию и mapping прав нужно проектировать отдельно
        - иначе команда платит за тяжёлый handshake без ясной access-модели
      - Где mTLS обычно не оправдан в публичном вебе
        - consumer web
        - first-time visitors
        - мобильные и гетерогенные клиенты
        - сценарии, где friction подключения разрушает пользовательский путь
      - Почему он там мешает
        - сертификат нужно доставить клиенту
        - сертификат нужно хранить и обновлять
        - ошибки становятся труднее для диагностики
        - latency и operability-цена заметны
      - Когда mTLS оправдан для service-to-service
        - контролируемый парк клиентов
        - автоматическая выдача сертификатов
        - высокая цена ложного доступа между сервисами
        - ingress/proxy -> внутренние сервисы
        - east-west traffic в service mesh
        - Prometheus -> exporter в закрытом контуре
        - job-runner -> API
        - multi-tenant среды с жёсткой service identity
      - Что mTLS даёт между сервисами
        - раннее отсечение неавторизованных источников на transport-уровне
        - удобную основу для zero-trust сегментации
      - Когда mTLS для service-to-service не окупается
        - очень короткоживущие внутренние контуры без внешней экспозиции
        - простые single-tenant среды с жёсткой сетевой изоляцией
        - временные dev-стенды
        - ситуации, где никто реально не проверяет SAN, issuer и role mapping
      - Практический критерий зрелости
        - команда должна уметь ответить:
          - кто именно этот клиент
          - какие вызовы ему разрешены
          - как быстро отзывается доступ
        - если этих ответов нет, mTLS ещё не означает завершённую security-модель
    - Приоритизация в эпоху HTTP/3
      - Исторический контекст
        - HTTP/2 предлагал сложную деревообразную схему приоритетов
        - практика показала, что она плохо совпадает с реальным планированием браузеров и серверов
      - Что изменилось
        - стандартизирован более прагматичный подход
        - Priority header и обновления приоритета работают для HTTP/2 и HTTP/3
      - Значение для HPBN
        - приоритизация снова становится инструментом доставки именно тех байтов, без которых браузер не продолжит работу
      - Смысл параметров
        - urgency u=0 означает максимальную срочность
        - большие значения означают "можно позже"
        - incremental i означает, что ресурс допустимо отдавать постепенно
      - Практическая политика выдачи
        - HTML верхнего уровня должен получать лучший класс обслуживания
        - блокирующие CSS должны идти раньше фоновых ресурсов
        - шрифты первого экрана важнее внеэкранных изображений
        - критические JSON/JS-зависимости важнее аналитики, prefetch и background updates
      - Ключевой вывод
        - сервер всё равно должен выбирать, что важнее
        - HTTP/3 делает policy-приоритизацию ещё важнее, потому что transport реже является главным тормозом
    - Как браузер узнаёт, что origin умеет HTTP/3
      - Проблема bootstrap
        - даже если сервер умеет HTTP/3, браузер должен сначала узнать об этом
      - Исторический путь через Alt-Svc
        - первый визит идёт по HTTP/1.1 или HTTP/2
        - сервер сообщает Alt-Svc
        - затем клиент пробует альтернативный сервис с ALPN h3
      - Ограничение Alt-Svc
        - механизм хорошо ускоряет последующие запросы
        - но не всегда помогает самому первому визиту
      - Более новый путь через HTTPS / SVCB
        - DNS может заранее сообщить параметры подключения и альтернативные endpoint'ы
        - transport discovery сдвигается ближе к DNS-фазе
        - шанс убрать лишний обход появляется раньше
      - Как сочетать оба механизма
        - Alt-Svc не устарел
        - система становится двухступенчатой
        - Alt-Svc остаётся рабочим механизмом объявления альтернативного сервиса
        - HTTPS/SVCB помогает сократить путь к тому же результату раньше
      - Практический вывод
        - реальное ускорение рождается не из одного "серебряного патрона"
        - а из цепочки небольших выигрышей вдоль connection path
  - Часть II: transport, visibility и fingerprints
    - Что объединяет вторую часть
      - современный веб не только передаёт данные
      - он измеряет, направляет и классифицирует соединения
      - trust-решения всё чаще принимаются ещё до полноценной application-логики
    - Мини-словарь второй части
      - 0-RTT = отправка данных до полного завершения handshake
      - replay = риск повторного воспроизведения раннего запроса
      - bottleneck bandwidth = полезная скорость самого узкого места пути
      - RTTprop = минимальный RTT без лишней очереди
      - qlog = подробный журнал событий QUIC
      - wire image = то, что сеть ещё способна увидеть снаружи
      - JA3 и JA4 = способы описать характерный отпечаток TLS-клиента
    - TLS 1.3 как сокращение критического пути
      - Почему TLS 1.3 важен для HPBN
        - он важен не только криптографией
        - он сокращает и упрощает установление защищённого канала
      - Что было у TLS 1.2
        - несколько дополнительных обменов
        - больше точек, где потери и очереди растягивали старт
      - Что делает TLS 1.3
        - убирает часть исторического багажа
        - делает handshake компактнее
      - Где выгода особенно заметна
        - мобильные сети
        - трансконтинентальные маршруты
        - удалённые origin
        - первые визиты после простоя
      - Ключевой тезис
        - TLS 1.3 не приближает сеть
        - но уменьшает число обязательных шагов до защищённых байтов
        - а любое сокращение обязательных RTT обычно ускоряет first render
    - 0-RTT: ускорение с оговорками
      - Что даёт 0-RTT
        - позволяет отправить ранние данные уже в первом сообщении
        - может срезать RTT на повторном визите
      - Почему это не универсальная победа
        - подходит только для безопасно повторяемых запросов
        - replay-риски нельзя игнорировать
        - неидемпотентные операции нельзя переносить в early data без разбора
      - Правильная инженерная стратегия
        - сначала добиться нормального resumption
        - затем рассматривать 0-RTT как надстройку для ограниченного набора маршрутов
      - Практический вывод
        - 0-RTT нужен для carefully-audited путей
        - он не должен становиться глобальным режимом всей системы
    - BBR: модель bottleneck bandwidth и RTT
      - Исторический контекст
        - раньше разговор о веб-производительности шёл через packet loss, slow start и loss-based congestion control
      - Что предлагает BBR
        - строить модель bottleneck bandwidth
        - оценивать round-trip propagation time
        - регулировать inflight data по модели пути, а не только по факту потерь
      - Почему это важно для HPBN
        - браузерная производительность чувствительна и к потерям, и к очередям
        - классические loss-based алгоритмы часто раздувают очередь до сигнала боли
        - BBR пытается держаться рядом с operating point пути
      - Что BBR может улучшить
        - уменьшить bufferbloat
        - сделать tail latency более предсказуемой
        - лучше балансировать throughput и RTT на части путей
      - Что BBR не гарантирует
        - автоматического ускорения на всех сетях
        - одинаково хорошего поведения для любых серверов и смесей трафика
        - идеальной fairness без оговорок
      - Практическая позиция
        - BBR надо тестировать как transport policy
        - нельзя воспринимать его как универсальный ответ
    - ECH: приватность ClientHello и косвенное влияние на веб
      - Главная цель ECH
        - скрыть SNI и другие чувствительные поля ClientHello
        - уменьшить видимость раннего этапа TLS
      - Почему это важно для HPBN
        - ECH не сокращает RTT напрямую
        - но меняет сетевую видимость и взаимодействие браузера, DNS и edge
      - Последствия для edge-инфраструктуры
        - старые диагностические и policy-приёмы на основе открытого SNI перестают работать
        - ранний bootstrap ECH опирается на HTTPS/SVCB DNS records
        - DNS становится частью transport story, а не просто прологом
      - Ключевой вывод
        - ECH влияет на приватность и управляемость пути
        - его ценность для HPBN связана с visibility и diagnostics, а не с прямым ускорением
    - Диагностика QUIC: что видно, а что уже не видно на проводе
      - Что меняется с QUIC
        - на wire image видно меньше, чем в мире TCP
        - это хорошо для целостности end-to-end транспорта
        - но хуже для пассивной диагностики
      - Что ещё можно оценивать пассивно
        - RTT в handshake
        - грубую RTT-оценку через spin bit
      - Что становится сложнее
        - пассивно измерять потери так же просто, как раньше, уже нельзя
        - восстановить внутреннее состояние transport извне становится трудно
      - Куда смещается observability
        - в endpoint logs
        - в qlog
        - в browser telemetry
        - в edge metrics
      - Что даёт qlog
        - packet numbers
        - ACK ranges
        - congestion state
        - handshake events
        - stream lifecycle
      - Новый практический принцип
        - одного packet capture недостаточно
        - для хорошей диагностики нужен набор источников: браузер, сервер, edge, qlog и ограниченный wire image
    - Fingerprinting клиента: от JA3 до canvas и поведения
      - Зачем используется fingerprinting
        - edge хочет понять не только IP и наличие потерь
        - но и тип клиента ещё до полноценной application-логики
      - Fingerprinting как многослойный процесс
        - часть сигнала видна очень рано
        - часть появляется только после запуска JavaScript
      - Ранние fingerprints
        - JA3 и JA4 описывают свойства TLS ClientHello
        - используются cipher suites, extensions, ALPN и другие детали приветствия
        - TCP fingerprint использует SYN-опции и транспортные особенности
        - HTTP/2 fingerprint смотрит на порядок SETTINGS и раннее поведение соединения
      - Почему ранние fingerprints важны
        - они доступны до отдачи HTML
        - решение можно принять ещё до запуска JavaScript
      - Поздние fingerprints
        - JavaScript runtime fingerprint
        - canvas и WebGL rendering
        - timing и behavioural patterns
        - ритм ввода, прокрутки и событий
        - особенности доступных API и их ответов
      - Чем поздние fingerprints отличаются
        - они богаче и точнее для anti-bot и fraud detection
        - но стоят CPU, времени и приватности
        - появляются только после старта страницы
      - Простое резюме
        - ранние fingerprints = портрет клиента на сетевом входе
        - поздние fingerprints = портрет браузера после начала работы страницы
    - Как fingerprints меняют производительность, маршрутизацию и метрики
      - Почему это тема HPBN, а не только security
        - fingerprinting живёт внутри реального delivery path
        - edge, CDN, WAF и anti-bot меняют дальнейшую судьбу запроса
      - Что может измениться из-за классификации клиента
        - запрос может пройти напрямую
        - запрос могут отправить на challenge
        - маршрут могут поменять
        - часть кэша могут отключить
        - rate limit могут изменить
        - соединение могут перевести в осторожный режим
      - Последствия для метрик
        - один и тот же пользователь может получить разный performance-path
        - latency зависит уже не только от сети и backend
        - на результат влияет trust decision на границе
      - Где fingerprinting помогает HPBN
        - защищает origin от мусорного трафика
        - уменьшает лишнюю нагрузку
        - позволяет смелее кэшировать доверенные классы клиентов
        - сокращает ненужные проверки для хорошего трафика
      - Где fingerprinting вредит HPBN
        - ложные срабатывания ухудшают UX
        - challenge и redirect удлиняют first visit
        - реальные пользовательские метрики становятся хуже
        - performance-сравнение без знания trust buckets становится некорректным
      - Практический вывод
        - fingerprinting нужно измерять так же серьёзно, как TLS handshake, кэширование, edge routing и retry policy
    - Fingerprinting и приватность пользователя
      - Обратная сторона технологии
        - чем больше сигналов собирается
        - тем легче различать добросовестный браузер и автоматизацию
        - но тем выше цена для приватности
      - Почему это чувствительнее cookie
        - fingerprinting может работать как пассивное узнавание клиента
        - пользователь не всегда ожидает настолько подробной идентификации
      - Что даёт более глубокий fingerprinting
        - более тонкую классификацию клиентов
        - лучшее anti-bot качество
        - возможность принять решение до тяжёлой application-логики
      - Чем платит пользователь
        - меньшей приватностью
        - большим числом проверок
        - потенциально худшим first paint на слабых устройствах
        - ложными challenge даже для хорошего трафика
      - Практический вывод
        - fingerprinting должен быть минимально достаточным
        - нужно собирать только сигналы, без которых нельзя принять решение
        - нельзя превращать приватность и производительность в побочный ущерб
    - Fingerprinting в архитектуре CDN / WAF / anti-bot
      - Типичная архитектура
        - часть сигнала видит CDN или edge proxy
        - часть собирает WAF на ранней фазе запроса
        - часть появляется в anti-bot JavaScript на клиенте
        - финальное решение принимает orchestration-логика
      - Хороший архитектурный вариант
        - дешёвые и простые сигналы обрабатываются рано на edge
        - challenge включается только для подозрительных bucket
        - performance-метрики ведутся отдельно по trust-классам и challenge-path
      - Плохой архитектурный вариант
        - любой клиент получает тяжёлый JS challenge
        - trust buckets непрозрачны аналитике
        - команда видит только "страница стала медленнее"
        - реальная доля времени в anti-bot не выделена
      - Ключевой вывод
        - CDN, WAF и anti-bot не находятся "рядом с сайтом"
        - они являются частью фактического пути запроса
        - а значит напрямую влияют на производительность
  - Практический набор для HPBN-команды
    - Базовые технические нормы
      - считать TLS 1.3 и resumption нормой, а не опциональной оптимизацией
      - включать 0-RTT только для replay-safe маршрутов
      - тестировать BBR как transport policy, а не как догму
    - Наблюдаемость и валидация
      - планировать observability QUIC заранее
      - собирать qlog, endpoint traces, browser telemetry и edge counters
      - отдельно валидировать UDP path
      - проверять таймауты NAT
      - проверять PMTU
      - проверять поведение middlebox
      - тестировать fallback к H2/TCP
    - Работа с fingerprinting
      - измерять долю клиентов в challenge path
      - измерять degraded mode
      - измерять альтернативные trust buckets
      - учитывать anti-bot как часть performance path
    - Главный итог
      - после 2016 года transport и безопасность сильно изменились
      - но правило HPBN осталось прежним
      - выигрывает тот стек, который быстрее и предсказуемее довозит браузеру байты для следующего шага
