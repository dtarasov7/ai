# Проект четвертый: Учебник по синтетической математике/кодированию

- Проект четвертый: Учебник по синтетической математике/кодированию
  - 1. Предыстория проекта (краткое описание проекта)
    - Определение задачи: Создайте высококачественный «Program of Thought» (PoT) Dataset. Мы используем LLM (DeepSeek-V3)…
    - Ввод и вывод: Ввод: Базовые наборы математических данных (например, GSM8K, MBPP) и необработанные файлы JSONL. Выход…
    - Анализ вызова: Самая большая трудность этого проекта заключается в «Устранение Hallucination». Код, сгенерированный LLM, часто выглядит правильно…
  - 2. Архитектурное проектирование (Архитектурное проектирование)
  - Data Pipeline Диаграмма
  - Технологический стек
    - Data Source: HuggingFace Datasets (GSM8K/MBPP).
    - Двигатель поколения: DeepSeek-V3 (через SiliconFlow API) — экономичная модель генерации кода.
    - Orchestration Логика: Скрипты Python (стратегия Evol-Instruct).
    - Среда проверки: Питон subprocess (локальная песочница) —— производство рекомендует Docker или MicroVM.
  - 3. Поэтапная реализация
  - Этап 1: Seed Сбор данных (подготовка Seed)
    - Все начинается с качественных семян.
    - Нам не нужны массивные данные — достаточно репрезентативных логических ядер.
    - Ключевые действия: 1.
    - Загрузите данные GSM8K (математические) и MBPP (код).
    - Код соединения (сэмплер данных): Код из download_data.py и sampler.py
  - Фаза 2: Генерация Evol-Instruct и PoT (Эволюция и поколение)
    - Это ядро ​​проекта.
    - Мы не можем просто создавать простые «пары вопросов и ответов» — нам нужна модель, которая будет думать как человек Expert.
    - Логика потока: 1.
    - Эвол (Эволюция): Перепишите простые задачи (например, «1+1=?») в сложные сценарии (например, «В Сяомине есть 1 яблоко, на которое влияет инфляция...»), добавив ограничения.
    - Основные подсказки (Prompt Engineering): Код из evol.py
  - Этап 3: Проверка в песочнице
    - Сгенерированные данные содержат большое количество «мертвых» выборок (синтаксическая ошибка, тайм-аут, цикл).
    - Необходимо проверить посредством выполнения.
    - Логика песочницы: 1.
    - Используйте Regex для извлечения блоков кода из Markdown.
    - Скрипт проверки: Код из sandbox.py
  - 4. Витрина результатов (Витрина)
    - После очистки песочницы получаем verified_textbook.jsonl .
    - Это synthetic data уровня учебника.
    - Сравнение образцов данных:
    - Фаза: Исходный Seed; Пример контента: У Дженни 5 яблок, она съела 2, сколько осталось?
    - Фаза: Эвол Эволюшн; Пример контента: У Дженни есть фруктовый магазин, в котором есть 5 ящиков яблок (по 12 на ящик). В понедельник она продала 40% запасов и 2 отдельных предмета…
    - Фаза: PoT Решение; Пример контента: def solve(): total = 5 * 12; sold = total * 0.4; ... return remaining
    - Фаза: Результат выполнения; Пример контента: 34 (проверено, сохранено в Dataset)
    - Статистика проверки: Обычно скорость однократного прохождения кода после Evol (Pass@1) составляет 60%-80% .
    - Данные об ошибках в 20 %, отфильтрованные в песочнице, — это именно те данные, которые могут испортить обучение модели. их удаление значительно улучшает логику модели SFT consistency.
  - 5. Стоимость и оптимизация (Стоимость и оптимизация)
    - Потребление ресурсов: API стоимость: Каждый действительный образец использует ~2 вызова LLM (эволюция + решение). Используя экономичные модели, такие как DeepSeek-V3…
    - Предупреждение безопасности (критическое): В этом проекте используется subprocess для местного исполнения. При обработке неизвестного или ненадежного кода, созданного моделью…
    - Рекомендации по масштабированию: Если данные масштабируются до миллионов, сценарий для одной машины не может поддерживаться. Нужно представить RabbitMQ или Kafka для распределения…
