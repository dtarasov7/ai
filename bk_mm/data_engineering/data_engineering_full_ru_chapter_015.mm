# Проект первый: создание распределенного «Mini-C4» Data Pipeline с Ray

- Проект первый: создание распределенного «Mini-C4» Data Pipeline с Ray
  - 1. Предыстория проекта (краткое описание проекта)
    - Определение задачи: Постройте миниатюру C4 (Колоссальный чистый прополз Corpus) Dataset Pipeline. Наша цель — преобразовать беспорядочные необработанные веб-данные (Common Crawl)…
    - Ввод и вывод: Ввод: Необработанные архивы WARC из Common Crawl (содержащие заголовки HTTP, HTML Source, искаженный текст и т. д.). Выход…
    - Анализ вызова: Очень низкое соотношение сигнал/шум: Более 90% необработанного веб-контента — это панели навигации, реклама…
  - 2. Архитектурное проектирование (Архитектурное проектирование)
    - Для обработки веб-данных Unstructured мы разработали следующую архитектуру обработки в форме воронки:
    - Data Pipeline Диаграмма:
    - Технологический стек:
    - Разбор Layer: Trafilatura Обоснование решения: По сравнению с традиционным BeautifulSoup, Trafilatura оптимизирован для веб-статьи Extraction, автоматически удаляя навигацию…
    - Вычислите Layer: Ray Обоснование решения: Встроенная многопроцессорность Python плохо справляется с большими данными. Ray предоставляет чрезвычайно простые распределенные примитивы…
    - Качество Layer: KenLM Обоснование решения: Облегченная библиотека N-грамм Language Model. В документах GPT-3 и CCNet используется KenLM Perplexity в качестве основного показателя…
  - 3. Поэтапная реализация
    - Этап 1. Извлечение основного содержимого из беспорядка HTML (Extraction и очистка)
      - Необработанные файлы WARC содержат большое количество нетекстового шума.
      - Сначала мы используем warcio для Streaming прочитайте сжатые архивы, затем используйте trafilatura для извлечения основного контента.
      - Основной код: синтаксический анализ и эвристическая очистка
    - Этап 2: Распространено MinHash Deduplication
      - В Интернете большое количество повторяющегося контента (репосты, зеркала).
      - Мы используем Ray для параллельных вычислений MinHash, объединяя LSH (с учетом местоположения Hashing) для уменьшения O( N 2 ) сложность для O(N) .
      - Основной код: Ray Параллельное вычисление подписи
    - Этап 3: Идентификация языка и Perplexity Filtering (качество Filtering)
      - Очищенные данные смешивают несколько языков с разным качеством.
      - Сначала мы используем FastText для языковой маршрутизации, затем KenLM для Perplexity.
      - Основной код: KenLM Оценка
  - 4. Витрина результатов (Витрина)
    - После этого Pipeline ландшафт данных фундаментально изменился:
    - Случай 1: Шум на панели навигации (удален)
    - Случай 2: фрагмент кода (удален)
    - Случай 3: Высококачественная статья (сохраненная и оцененная)
    - Статистика данных: В выборочном тесте с одним сканированием: * Сырые записи: 10 000 * Извлеченный действительный текст: ~4500 (HTML анализ Loss) * Осталось после чистки…
