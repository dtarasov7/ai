# Проект третий: Строительство LLaVA мультимодального Instruction Dataset

- Проект третий: Строительство LLaVA мультимодального Instruction Dataset
  - 1. Предыстория проекта (краткое описание проекта)
    - Определение задачи: Создайте высококачественный визуальный элемент instruction fine-tuning Dataset с поддержкой одного изображения QA (Visual QA)…
    - Ввод и вывод:
    - Ввод: Библиотека необработанных изображений ( .jpg / .png ) Структурированные данные Annotation (например, формат COCO instances.json с координатами Bbox)
    - Выход: Файлы JSON, соответствующие стандарту обучения LLaVA (с image , conversations поля). Grounding данные с координатой Normalization и форматом Alignment.
    - Анализ вызова:
    - Координата Alignment (Координата Alignment): Координаты необработанных данных обнаружения обычно представляют собой абсолютные значения пикселей (x, y, w, h)…
    - Многообразная логическая конструкция: Традиционные данные Image-Caption — это одно изображение и один текст…
