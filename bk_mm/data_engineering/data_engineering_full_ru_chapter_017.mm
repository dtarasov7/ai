    - Чтобы научить модель «сравнивать» два изображения, мы используем API для динамического ввода нескольких изображений и запроса сравнения.
    - Ключевая логика: Используйте VLM API для создания входных данных из нескольких изображений Prompt.
    - Это основная математика проекта.
    - COCO использует [x_topleft, y_topleft, width, height] в то время как LLaVA требуется [ymin, xmin, ymax, xmax] со значениями, нормализованными до целых чисел 0–1000.
    - Ключевая функция: Преобразование координат Normalization
    - Генерация данных не должна идти непосредственно на обучение.
    - Должен пройти обратная проверка визуализации .
    - Логика проверки: Разобрать сгенерированный JSON, восстановить [0-1000] координаты в пиксельные координаты и рисовать.
