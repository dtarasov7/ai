
- Глава 13: Multimodal RAG
  - Краткое содержание главы
    - В условиях пика конкуренции за текст RAG мультимодальные перевозки стали новым полем битвы.
    - Корпоративные документы часто содержат большие объемы диаграммы, блок-схемы, скриншоты — традиционный RAG обычно выбирает OCR для текста или игнорирует изображения, в результате чего ключевая информация Loss.
  - 13.0 Цели обучения
