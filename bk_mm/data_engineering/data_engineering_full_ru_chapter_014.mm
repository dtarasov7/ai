    - Мастер-архитектура ColPali : Понять механизм позднего взаимодействия; научиться использовать colpali-v1.2-merged для сложных PDF таблиц и диаграмм.
    - Инженерные возможности : Написание реализации кода Python МаксСим скоринг алгоритм и использование Двоичный Quantization сократить стоимость хранения в 32 раза.
    - Проверка визуализации : Проверьте интерпретируемость модели с помощью тепловых карт Attention.
  - Введение в сценарий
    - Вы занимаетесь ремонтом полупроводникового оборудования knowledge base.
    - Технические руководства полны сложных принципиальных схем и структурных схем устройств.
    - Традиционный RAG (только текст) : Получено текстовое описание «Источник питания материнской платы см. Рисунок 3-12», но система не может вернуть изображение…
    - Multimodal RAG : Система не только понимает текстовое намерение пользователя, но и напрямую извлекает скриншоты страниц PDF, содержащих принципиальную схему…
    - Это качественный скачок от «чтения» к «видению».
    - Во многих промышленных и финансовых сценариях плотность информации одного изображения намного превышает тысячу слов.
  - 13.1 Кросс-модальный поиск: преодоление барьеров между текстом и изображением
    - Чтобы реализовать «поиск текста в изображении» или «поиск изображения в тексте», нам нужна модель, которая понимает обе модальности.
    - 13.1.1 Основной принцип: Contrastive Learning
      - CLIP OpenAI (контрастный языковой образ Pre-Training) является краеугольным камнем.
      - Логика его обучения проста и жестока: 1.
      - Результат: вектор фотографии «собака» и текстовый вектор «собака» математически очень близки в пространстве.
    - 13.1.2 Выбор технологии: CLIP или SigLIP
      - Хотя CLIP наиболее известен, для разработки у нас есть лучший выбор.
      - Google SigLIP (сигмовидная Loss для языкового образа Pre-Training) превосходит CLIP по нескольким показателям.
      - Feature: Loss Function; OpenAI CLIP: Softmax (глобальный Normalization); Google SigLIP: сигмовидная (независимая бинарная классификация)…
      - Feature: Китайская поддержка; OpenAI CLIP: Слабый (в основном английский); Google SigLIP: Лучше (многоязычная версия); Рекомендация архитектора: Необходимо использовать многоязычный Checkpoint
      - Feature: Разрешение; OpenAI CLIP: Обычно 224x224; Google SigLIP: Поддерживает динамическое разрешение; Рекомендация архитектора: Для сложных диаграмм выбирайте модель с высоким разрешением (384+)
  - 13.2 ColPali Архитектура на практике: конец кошмару OCR
    - Для поиска документов PDF CLIP имеет фатальную слабость: он превосходно справляется с естественными изображениями (кошки, собаки, пейзажи)…
    - ColPali (КолБЕРТ + ПалиГемма) предлагает революционную идею: Нет OCR — страницы PDF рассматриваются непосредственно как изображения.
    - 13.2.1 Основной принцип: позднее взаимодействие моделей визуального языка
      - ColPali сочетает в себе Vision-language Model (VLM) и механизм поиска ColBERT.
      - Патч Embedding : Split изображение документа на небольшие фрагменты; каждый патч генерирует вектор. Одно изображение может соответствовать 1024 векторам.
      - Позднее взаимодействие (MaxSim) : При извлечении вычисляйте каждый вектор Query Token по всем векторам патчей документа, принимайте максимальное сходство.
  - 13.3 Инженерная реализация: построение гибридного мультимодального поиска Pipeline
    - В этом разделе реализована структура поисковой системы, совместимая с SigLIP (естественные изображения) и ColPali (изображения документов) .
    - 13.3.1 Общая архитектура и поток данных
      - Перед кодированием нам необходимо уточнить поток данных.
      - Это уже не просто «ввод текста, вывод текста».
    - 13.3.2 Основной код: мультимодальный Indexing и оценка
      - Мы определяем MultimodalIndexer .
      - Для практических возможностей нам нужна не просто логика «Vectorization (Embedding)», но и явная логика «оценки» — поиск ColPali не может просто полагаться на Vector Database Cosine Similarity.
  - 13.3.3 Оптимизация производительности: двоичный Quantization (BQ)
    - Самая большая инженерная проблема ColPali — это взрыв хранилища .
    - Традиционный Embedding : 1 страница = 1 вектор (4 КБ Float32).
    - ColPali : 1 страница = 1032 вектора (512 КБ Float16).
    - Indexing 1 миллиону страниц PDF требуется 500 ГБ памяти, что на практике часто неприемлемо.
    - Решение : использовать двоичный файл Quantization, сжимая Float16 до 1 бита.
  - 13.4 Производительность и оценка (Производительность и оценка)
    - Оценка Multimodal RAG — это не просто «найти правильно», но и «почему это было найдено».
    - 13.4.1 Метрики оценки
      - Метрика: Recall@К; Применимый сценарий: Общий поиск; Описание: Вероятность того, что первые K результатов содержат правильное изображение.
      - Метрика: NDCG@10; Применимый сценарий: Ranking качество; Описание: Чем выше оценка, тем более релевантные изображения имеют более высокий рейтинг.
      - Метрика: OCR-Бесплатная эффективность; Применимый сценарий: сценарий ColPali; Описание: Соотношение экономии времени и затрат по сравнению с OCR + Dense Retrieval.
    - 13.4.2 Контрольные показатели
      - Тестовая среда : Intel Xeon Gold 6226R, NVIDIA RTX 3090.
      - Dataset : ВиДоРе Benchmark (сложная финансовая отчетность).
      - 1. Сравнение точности (Recall@5)
        - Unstructured OCR + BGE-M3 : 43% (основная причина – структура таблицы Loss).
        - ColPali v1.2 : 81% (непосредственное визуальное понимание макета).
      - 2. Сравнение задержки
        - SigLIP (Плотный) : < 20 мс/Query.
        - ColPali (позднее взаимодействие) : ~150 мс / Query.
        - Заключение : ColPali подходит для повторного ранжирования или высококачественного поиска; для больших данных требуется Quantization.
    - 13.4.3 Интерпретируемость: куда смотрит модель?
      - Еще одним преимуществом ColPali является интерпретируемость .
      - Визуализируя матрицу взаимодействия из вычислений MaxSim, мы можем создавать тепловые карты, показывающие, какую именно область документа обрабатывает модель.
