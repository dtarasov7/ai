    - 3.17. Защита от атак, направленных на отказ в обслуживании (ОО)
      - Цель: предотвратить или снизить последствия DoS/DDoS-атак
      - Требуются
        - мониторинг доступности
        - фильтрация трафика
        - взаимодействие с провайдерами
        - ограничение аномальной нагрузки
      - Нужны
        - сценарии реагирования
        - каналы эскалации
        - планы восстановления доступности
      - Меры должны учитывать критичность сервисов и допустимое время простоя
    - 3.18. Защита при использовании искусственного интеллекта
