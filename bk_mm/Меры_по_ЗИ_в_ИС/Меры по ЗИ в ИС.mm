      - В приложениях приводится сводная таблица базовых мер и усилений
    - 4.1. Идентификация и аутентификация (ИАФ)
      - Идентификация пользователей
        - Нужно исключить доступ
          - лиц, не являющихся пользователями системы
        - Категории пользователей
          - внутренние
          - внешние
          - привилегированные
          - непривилегированные
          - пользователи подрядчиков
        - Требуются этапы идентификации
          - первичная идентификация
          - вторичная идентификация
        - Идентификатор должен однозначно сопоставляться с пользователем и запускаемыми от его имени процессами
        - Должно быть управление идентификаторами
          - создание
          - присвоение
          - обновление
          - блокировка
          - отзыв
      - Идентификация устройств
        - Требуется определить типы
          - сетевых устройств
          - серверов
          - рабочих мест
          - мобильных устройств
          - систем хранения
        - Устройства должны иметь уникальные признаки
          - имена
          - IP-адреса
          - MAC-адреса
          - комбинации признаков
        - Вторичная идентификация выполняется при каждом запросе подключения
        - Усиления включают
          - инвентаризацию идентификационных данных
          - сертификаты
          - централизованное управление жизненным циклом идентификаторов
      - Аутентификация пользователей
        - Проверяется принадлежность предъявленного идентификатора конкретному пользователю
        - Могут применяться
          - пароли
          - одноразовые коды
          - сертификаты
          - электронные идентификаторы
          - многофакторные механизмы
        - Требуются правила
          - хранения аутентификационных данных
          - выдачи аутентификационных данных
          - смены аутентификационных данных
          - блокировки аутентификационных данных
      - Аутентификация устройств
        - Устройства должны подтверждать подлинность при подключении
        - В усиленных вариантах применяются
          - сертификаты безопасности
          - взаимная аутентификация
    - 4.2. Управление доступом (УПД)
      - Реализация модели управления доступом
        - Модель определяет
          - субъекты доступа
          - объекты доступа
          - виды доступа
          - правила принятия решений
        - Возможны методы управления доступом
          - дискреционный
          - ролевой
          - мандатный
          - атрибутный
      - Разграничение и контроль прав доступа
        - Доступ предоставляется по принципу минимальных привилегий
        - Права должны соответствовать обязанностям пользователя и регулярно пересматриваться
      - Управление учетными записями
        - Требуются операции с учетными записями
          - создание
          - изменение
          - блокировка
          - удаление
          - учет
        - Особое внимание уделяется учетным записям
          - привилегированным
          - временным
      - Ограничение неуспешных и нерегламентированных попыток доступа
        - Должны задаваться
          - пороги неуспешных входов
          - условия блокировки
          - уведомления
      - Предупреждение пользователя при доступе
        - Пользователь информируется о правилах работы и контроле действий
      - Оповещение о предыдущем входе
        - Пользователь может видеть сведения о прошлой успешной аутентификации
      - Ограничение параллельных сеансов
        - Снижается риск одновременного использования одной учетной записи
      - Блокирование сеанса при неактивности
        - Сеанс завершается или блокируется после установленного времени бездействия
      - Действия до идентификации и аутентификации
        - Должны быть строго определены операции
          - разрешенные до прохождения контроля доступа
