# Меры по ЗИ в ИС

- Методический документ ФСТЭК России: состав и содержание мероприятий и мер по защите информации в информационных системах
  - Общая характеристика документа
    - PDF: `Меры по ЗИ в ИС.pdf`
    - Документ утвержден ФСТЭК России 12 апреля 2026 г.
    - Полное название: состав и содержание мероприятий и мер по защите информации, содержащейся в информационных системах
    - Документ развивает требования по защите информации и описывает практический состав процессов и мер защиты
    - Объем PDF: 182 страницы
    - Извлечение текста осложнено
      - часть шрифтов PDF не имеет корректной Unicode-карты
      - структура mindmap построена по восстановленному тексту
      - дополнительно учтены видимые разделы и формулировки документа
  - Назначение документа
    - Определяет общие подходы к составу и содержанию мероприятий по защите информации
    - Описывает меры защиты информации, применяемые в информационных системах
    - Детализирует процессы, которые должны реализовываться в органе или организации для достижения целей защиты
    - Учитывает задачи защиты информации и обеспечения безопасности значимых объектов критической информационной инфраструктуры
    - Не рассматривает выбор и реализацию криптографических мер защиты, которые регулируются требованиями ФСБ России
  - Область применения
    - Информационные системы
      - государственных органов
      - государственных унитарных предприятий
      - государственных учреждений
      - организаций
    - Автоматизированные системы управления
    - Информационно-телекоммуникационные сети
    - Инфраструктуры, обеспечивающие технологическую основу функционирования информационных систем
    - Значимые объекты критической информационной инфраструктуры
    - Средства, используемые для создания и эксплуатации государственных информационных систем
      - технические средства
      - программы для ЭВМ
      - базы данных
  - Участники и адресаты
    - Обладатели информации
    - Заказчики, заключившие государственные контракты на создание информационных систем
    - Операторы информационных систем
    - Подрядные организации, которым передаются данные или предоставляется доступ к системам
    - Организации, выполняющие работы с информационными системами
      - обработка информации
      - хранение информации
      - создание систем
      - развитие систем
      - эксплуатация систем
      - защита систем
  - Когда применяется документ
    - При организации деятельности по защите информации
    - При создании системы защиты информации органа или организации
    - При управлении системой защиты информации
    - При создании, эксплуатации и сопровождении информационных систем
    - При поддержании необходимого уровня защищенности
    - При оценке эффективности деятельности по защите информации
    - При аттестации информационных систем и иных формах оценки соответствия
  - Общие положения
    - Защита должна рассматриваться как непрерывная деятельность, а не разовая процедура
    - Меры защиты принимаются на всех стадиях жизненного цикла информационной системы
    - Требуется учитывать цели защиты, актуальные угрозы и возможные негативные последствия
    - Должны использоваться внутренние документы
      - политики
      - стандарты
      - регламенты
      - эксплуатационная документация
    - Документ заменяет прежние методические положения по мерам защиты информации в государственных информационных системах от 2014 года
  - Факторы, влияющие на состояние защиты информации
    - Цифровая зрелость органов и организаций повышает зависимость функций от устойчивости информационных систем
    - Эффективность защиты зависит от
      - внедрения процессов
      - компетенций специалистов
      - качества средств защиты
      - полноты документации
    - Ответственные лица должны обладать необходимыми компетенциями и регулярно повышать квалификацию
    - При нехватке собственных специалистов допускается привлечение лицензированных организаций, но полномочия и ответственность должны быть явно разделены
    - Используемые программные и программно-аппаратные средства должны поддерживать реализацию мер защиты и не создавать новых угроз
    - Регламенты и стандарты защиты должны учитывать особенности деятельности органа и архитектуры информационных систем
    - Работники должны быть информированы о политике защиты информации и внутренней нормативной базе
    - Защита должна закладываться на этапе проектирования, а не добавляться только после ввода системы в эксплуатацию
    - Архитектурные решения должны выбираться на основе моделирования угроз и описания поверхности компьютерных атак
    - Важны архитектурные принципы защиты
      - минимизация поверхности атаки
      - сегментация
      - безопасные конфигурации
      - контролируемое взаимодействие компонентов
    - Доступность сервисов через интернет увеличивает требования к контролю внешних интерфейсов и сетевого взаимодействия
    - Рост числа сервисов и функций информационных систем повышает сложность управления доступом, событиями и уязвимостями
    - Использование зарубежного ПО и оборудования требует учета рисков
      - поставки
      - сопровождения
      - обновлений
      - доверия
    - Большое число субъектов и объектов доступа требует формализованной модели управления доступом
    - Качество текущего состояния защиты должно оцениваться по
      - зрелости процессов
      - документированности
      - применяемым средствам
      - результатам контроля
  - Мероприятия и процессы по защите информации
    - Общая логика раздела
      - Мероприятия описывают организационные и управленческие процессы защиты информации
      - Для каждого мероприятия задаются
        - цель
        - требования к реализации
        - требования к документированию
        - возможные усиления
      - Усиления применяются по решению
        - оператора
        - обладателя информации
      - Назначение усилений
        - повышение эффективности защиты
        - повышение уровня защищенности
        - снижение возможностей нарушителя
    - 3.1. Выявление и оценка угроз безопасности информации (ВУ)
      - Цель
        - создать систему защиты, ориентированную на актуальные угрозы
        - своевременно выявлять признаки реализации угроз
        - оценивать выявленные угрозы
        - принимать меры по защите информации
      - На стадии создания системы формируется модель угроз с учетом
        - архитектуры
        - технологий
        - процессов обработки
        - возможностей нарушителей
      - На стадии эксплуатации выполняются
        - анализ состава системы
        - анализ настроек
        - анализ конфигураций
        - поиск признаков актуальных угроз
        - приоритизация угроз
      - В качестве источников используются
        - банк данных угроз ФСТЭК России
        - сведения об уязвимостях
        - сведения о нарушителях
        - сведения о методах атак
      - Усиления включают
        - учет данных инвентаризации ИТ-активов
        - обогащение данными SIEM
        - обогащение данными IDS
        - обогащение данными AV
        - обогащение данными EDR
        - обогащение данными межсетевых экранов
        - применение TI-платформ
    - 3.2. Контроль конфигураций информационных систем (КК)
      - Цель
        - исключить несанкционированное изменение состава компонентов
        - исключить несанкционированное изменение настроек
        - исключить несанкционированное изменение конфигураций
        - своевременно обнаруживать факты изменений
        - выявлять причины изменений
      - Контроль строится на актуальных данных об объектах инвентаризации
      - Объектами считаются
        - программные средства
        - программно-аппаратные средства
        - коммуникационное оборудование
        - сети
        - подсети
      - Требуются операции с данными об объектах
        - сбор
        - учет
        - хранение
        - актуализация
      - Должны выявляться факты
        - несанкционированного добавления объектов
        - несанкционированного изменения текущих конфигураций
        - несанкционированного изменения состава объектов инвентаризации
      - Документирование включает внутренние стандарты с описанием
        - типовых конфигураций
        - ответственных лиц
        - порядка действий при изменениях
      - Усиление
        - автоматизированный сбор данных об объектах и конфигурациях
        - автоматизированное хранение данных об объектах и конфигурациях
        - выполнение операций с минимально необходимыми правами
    - 3.3. Управление уязвимостями (КУ)
      - Цель
        - своевременно выявлять уязвимости
        - оценивать критичность уязвимостей
        - выбирать методы устранения
        - определять приоритеты устранения
        - контролировать исправление
      - Процесс включает
        - мониторинг уязвимостей
        - оценку применимости
        - оценку критичности
        - приоритизацию
        - устранение
        - контроль устранения
      - Источниками являются
        - внешние базы уязвимостей
        - официальные ресурсы разработчиков
        - специализированные публикации
        - форумы
        - средства анализа защищенности
        - данные о составе активов
      - Методы устранения включают
        - обновление ПО
        - компенсирующие меры защиты
      - Регламент должен описывать
        - роли
        - операции
        - входные данные
        - выходные данные
        - сроки
        - схемы взаимодействия подразделений
      - Усиления
        - автоматизированные системы управления уязвимостями
        - TI-платформы
        - данные CMDB
        - данные инвентаризации
        - связь с мониторингом ИБ
    - 3.4. Управление обновлениями (КО)
      - Цель
        - своевременно устанавливать обновления ПО
        - устранять уязвимости через обновления
        - снижать риск эксплуатации известных уязвимостей
      - Процесс включает
        - контроль актуальности версий
        - получение обновлений из доверенных источников
        - проверку подлинности обновлений
        - проверку целостности обновлений
      - Обновления должны тестироваться до применения в промышленной эксплуатации
      - Должна существовать возможность отмены обновления или восстановления из резервной копии
      - Решения об обновлениях принимаются совместно подразделениями эксплуатации и защиты информации
      - Для распространения обновлений должен использоваться выделенный сервер или сегмент обновлений
      - Онлайн-загрузка допускается только при проверке целостности и аутентичности на основе сертификатов безопасности
      - Усиления
        - песочница для тестирования
        - данные инвентаризации
        - выделенный источник обновлений
    - 3.5. Защита информации ограниченного доступа (ОД)
      - Цель
        - исключить неправомерное распространение информации ограниченного доступа
        - защитить информацию при обработке
        - защитить информацию при хранении
        - защитить информацию при обращении
      - Нужно определить
        - перечень информации ограниченного доступа
        - средства, предназначенные для хранения такой информации
        - лиц, которым доступ разрешен
        - порядок контроля обращения с информацией
      - Доступ разрешается только лицам, которым он необходим по внутренним регламентам
      - Требуется контроль передачи, распространения и вывода информации из информационной системы
      - К информации ограниченного доступа относятся
        - ДСП
        - персональные данные
        - коммерческая тайна
        - иные сведения с ограниченным доступом
      - При выводе носителей из эксплуатации требуется уничтожение информации или физическое уничтожение носителей
      - Регламент должен описывать
        - перечни информации
        - перечни носителей
        - допущенных лиц
        - порядок учета
        - порядок уничтожения
        - порядок контроля
        - порядок расследования нарушений
    - 3.6. Защита при использовании конечных устройств (ЗУ)
      - Цель
        - обеспечить безопасное использование рабочих станций
        - обеспечить безопасное использование серверов
        - обеспечить безопасное использование терминалов
        - обеспечить безопасное использование иных конечных устройств
      - Важны
        - контроль доступа
        - целостность ПО
        - антивирусная защита
        - мониторинг процессов
        - сетевой трафик конечного устройства
      - Требуется учитывать
        - подключение внешних устройств
        - попытки удаленного доступа
        - запуск процессов
        - изменение конфигурации
      - Документирование должно связывать конечные устройства с
        - ролями
        - владельцами
        - конфигурациями
        - требованиями эксплуатации
    - 3.7. Защита при применении мобильных устройств
      - Цель
        - снизить риски доступа к информационным системам с мобильных устройств
        - ограничить утечки через мобильные устройства
        - контролировать мобильную среду доступа
      - Нужно управлять
        - регистрацией устройств
        - доступом
        - приложениями
        - целостностью ОС
        - данными
      - Требуются ограничения на
        - хранение данных
        - резервное копирование в публичные облака
        - снимки экрана
        - передачу устройства другому пользователю
      - Усиления включают
        - MDM
        - централизованное управление приложениями
        - удаленное стирание
        - контроль геопозиции
    - 3.8. Защита при удаленном доступе пользователей (УД)
      - Цель: обеспечить безопасный доступ пользователей вне контролируемой зоны
      - Требуется
        - идентификация
        - аутентификация
        - разграничение прав
        - защита каналов связи
        - контроль действий удаленных пользователей
      - Удаленный доступ должен быть
        - регламентирован
        - ограничен по ролям
        - предоставляться только при необходимости
      - Нужны
        - контроль сеансов
        - регистрация событий
        - возможность прекращения доступа
    - 3.9. Защита при беспроводном доступе (БД)
      - Цель: контролировать подключение пользователей и устройств по беспроводным каналам
      - Требуются идентификация и аутентификация субъектов беспроводного доступа
      - Нужно управлять
        - точками доступа
        - сегментацией
        - уровнем сигнала
        - разрешенными устройствами
        - протоколами
      - Важны
        - мониторинг подключений
        - выявление несанкционированных точек доступа
        - контроль покрытия
    - 3.10. Защита при предоставлении привилегированного доступа (ПД)
      - Цель: снизить риски администрирования и доступа с повышенными правами
      - Привилегированный доступ должен предоставляться только по необходимости и по принципу минимальных привилегий
      - Требуются
        - учет
        - контроль
        - регистрация действий
        - ограничение сессий
        - регулярный пересмотр прав
      - Должны применяться
        - отдельные учетные записи
        - усиленная аутентификация
        - контроль административных операций
    - 3.11. Мониторинг информационной безопасности
      - Цель: обеспечить наблюдение за событиями, признаками атак и состоянием защищенности
      - Источники мониторинга включают
        - журналы событий
        - средства защиты
        - сетевые устройства
        - конечные устройства
        - прикладные сервисы
      - Требуются
        - корреляция
        - анализ
        - оповещение
        - реагирование
        - документирование результатов
      - Усиления могут включать
        - SIEM
        - центры мониторинга ИБ
        - ретроспективный анализ
        - обогащение данными угроз
    - 3.12. Разработка безопасного программного обеспечения (БР)
      - Цель: снизить число уязвимостей, закладываемых в ПО на этапе разработки
      - Требуются
        - безопасные практики разработки
        - анализ кода
        - тестирование
        - управление зависимостями
        - проверка перед внедрением
      - Должны учитываться требования к
        - архитектуре
        - конфигурациям
        - интерфейсам
        - обработке ошибок
      - Важны
        - регламенты безопасной разработки
        - разграничение сред
        - контроль изменений
    - 3.13. Физическая защита информационных систем (ФЗ)
      - Цель: исключить физический несанкционированный доступ к компонентам и носителям
      - Требуются
        - контролируемые зоны
        - учет доступа в помещения
        - защита оборудования
        - защита носителей
      - Нужно защищать
        - серверные
        - рабочие места
        - телекоммуникационные шкафы
        - точки доступа
      - Должны быть определены
        - ответственные
        - порядок допуска
        - контроль физического доступа
    - 3.14. Непрерывность функционирования при нештатных ситуациях (НФ)
