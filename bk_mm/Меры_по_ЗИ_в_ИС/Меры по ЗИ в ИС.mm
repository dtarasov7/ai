          - удаленный доступ
          - внешние устройства
          - изменение конфигурации
      - Контроль и фильтрация трафика
        - Фильтрация выполняется по
          - IP-адресам
          - портам
          - протоколам
          - содержимому
          - правилам межсетевого экрана
      - Анализ и реагирование на события безопасности
        - События конечных устройств анализируются и передаются в процессы реагирования
