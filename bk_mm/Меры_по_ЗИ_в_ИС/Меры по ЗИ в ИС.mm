        - контроль целостности резервных копий
        - изоляция резервной инфраструктуры
    - 3.15. Повышение знаний и информированности пользователей (УЗ)
      - Цель: снизить риски, связанные с ошибками пользователей и незнанием требований ИБ
      - Требуются
        - обучение
        - информирование о политиках
        - информирование о правилах работы
